        Heirarchical Databases
            + Computerized databases systems were first introduced in the early 1960s. The most successful of the first database
            systems was the SABRE storage methodology introduced by IBM. 
            + Note that these early databases were hierarchical in
            nature; this meant that all entries were stored with some kind of a connection to a parent node. These types of databases
            are very simple in nature. They resemble the structure of tree data stuctures, as well as directories within a computer
            system.
            + Data could only have parent/child relationships. A child could only have one parent, but parent multiple childs
            + In many cases a many-to-one relationship would be highly benefitial; ie two parents having relationships to one child

        Relational Databases
        - The first relational database model was proposed back in the 1970s as an alternative to hierarchical models. 


